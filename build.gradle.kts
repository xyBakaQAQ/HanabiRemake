//Constants:

val baseGroup: String by project
val mcVersion: String by project
val version: String by project
val mixinGroup = "$baseGroup.mixin"
val modid: String by project
val transformerFile = file("src/main/resources/accesstransformer.cfg")

// Toolchains:
java {
    toolchain {
        languageVersion.set(JavaLanguageVersion.of(8))
        // Pin to Azul Zulu 8: its JDK bundles JavaFX (jfxrt.jar), which the cloud-music
        // player (javafx.scene.media) needs at compile time. Temurin 8 does NOT ship
        // JavaFX, so without this Gradle may pick Temurin and the build fails.
        vendor.set(JvmVendorSpec.AZUL)
    }
}

// Locate JavaFX (jfxrt.jar) from the Azul Zulu 8 toolchain. Forge's LaunchClassLoader does
// NOT read the JDK's extension dir, so the cloud-music player (javafx.scene.media) needs
// jfxrt.jar explicitly on the runtime classpath - otherwise the client crashes on startup
// with NoClassDefFoundError: javafx/scene/media/MediaPlayer.
val jfxrtJar = extensions.getByType(org.gradle.jvm.toolchain.JavaToolchainService::class.java)
    .launcherFor {
        languageVersion.set(JavaLanguageVersion.of(8))
        vendor.set(JvmVendorSpec.AZUL)
    }
    .map { it.metadata.installationPath.file("jre/lib/ext/jfxrt.jar") }

// Minecraft configuration:
loom {
    log4jConfigs.from(file("log4j2.xml"))
    launchConfigs {
        "client" {
            // If you don't want mixins, remove these lines
            property("mixin.debug", "true")
            arg("--tweakClass", "org.spongepowered.asm.launch.MixinTweaker")
        }
    }
    runConfigs {
        "client" {
            if (SystemUtils.IS_OS_MAC_OSX) {
                // This argument causes a crash on macOS
                vmArgs.remove("-XstartOnFirstThread")
            }
        }
        remove(getByName("server"))
    }
    forge {
        pack200Provider.set(dev.architectury.pack200.java.Pack200Adapter())
        // If you don't want mixins, remove this lines
        mixinConfig("mixins.$modid.json")
	    if (transformerFile.exists()) {
			println("Installing access transformer")
		    accessTransformer(transformerFile)
	    }
    }
    // If you don't want mixins, remove these lines
    mixin {
        defaultRefmapName.set("mixins.$modid.refmap.json")
    }
}

